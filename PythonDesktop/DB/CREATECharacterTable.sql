	CHR INTEGER PRIMARY KEY AUTOINCREMENT, 
	キャラ名 TEXT UNIQUE,
	性別 BLOB,
	所属 TEXT,
	HP_成長 TINYINT,
	力_成長 TINYINT,
	魔力_成長 TINYINT,
	技_成長 TINYINT,
	速さ_成長 TINYINT,
	幸運_成長 TINYINT,
	守備_成長 TINYINT,
	魔防_成長 TINYINT,
	魅力_成長 TINYINT,
	HP_上限 TINYINT,
	力_上限 TINYINT,
	魔力_上限 TINYINT,
	技_上限 TINYINT,
	速さ_上限 TINYINT,
	幸運_上限 TINYINT,
	守備_上限 TINYINT,
	魔防_上限 TINYINT,
	魅力_上限 TINYINT
	)