CREATE TABLE IF NOT EXISTS CurrentUnits (
	UNIT_NO INTEGER PRIMARY KEY AUTOINCREMENT, 
	CHR INTEGER,
	CLS INTEGER,
	LVL TINYINT,
	有効 TEXT,
	HP TINYINT,
	力 TINYINT,
	魔力 TINYINT,
	技 TINYINT,
	速さ TINYINT,
	幸運 TINYINT,
	守備 TINYINT,
	魔防 TINYINT,
	魅力 TINYINT,
	技能_剣術 TINYINT,
	技能_槍術 TINYINT,
	技能_斧術 TINYINT,
	技能_弓術 TINYINT,
	技能_格闘術 TINYINT,
	技能_理学 TINYINT,
	技能_信仰 TINYINT,
	技能_指揮 TINYINT,
	技能_重装 TINYINT,
	技能_馬術 TINYINT,
	技能_飛行 TINYINT,
	FOREIGN KEY(CHR) REFERENCES Characters(CHR), 
	FOREIGN KEY(CLS) REFERENCES Class(CLS),
	FOREIGN KEY(有効) REFERENCES Class(有効),
	)